#include <stdio.h>
/**
 * main - starts the program
 * Description - prints the alphabet
 * Return: 0 is the fuction finish correctly
 */
int main(void)
{
	int ch;

	for (ch = 'a'; ch <= 'z'; ch++)
	{
		putchar(ch);
	}
	for (ch = 'A'; ch <= 'Z'; ch++)
	{
		putchar(ch);
	}
	putchar('\n');
	return (0);
}
